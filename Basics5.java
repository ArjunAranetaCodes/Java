//Basic Operations
public class arithmetic_operations{
 public static void main(String[]args){
  System.out.println("1 + 1 = " + (1 + 1));
  System.out.println("1 - 1 = " + (1 - 1));
  System.out.println("1 * 1 = " + (1 * 1));
  System.out.println("1 / 1 = " + (1 / 1));
  System.out.println("4 % 2 = " + (4 % 2) +
  " there is no remainder");
  System.out.println("5 % 2 = " + (5 % 2) +
  " there is 1 remainder");
 }
}
